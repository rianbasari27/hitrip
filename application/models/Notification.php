    public function setToken($token, $id, $user) {
        
        $this->db->where('token', $token);
        $query = $this->db->get('notif');
        $data = $query->row();
        $tokenData = array (
            'token' => $token,
            'user_id' => $id,
            'user' => $user
        );

        if (empty($data)) {
                $this->db->insert('notif', $tokenData);
                return true;
        } else {
            unset($tokenData['token']);
            $this->db->where('token', $token);
            $this->db->update('notif', $tokenData);
            return false;
        }
    }

